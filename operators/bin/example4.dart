// Binary Infix Opearators
void main(List<String> args) {
  int number = 50;

  print(number + 20);
  print(number - 20);
  print(number * 20);
  print(number / 20);
  print(number ~/ 20);
  print(number % 20);
  print('');

  print(number == 20);
  print(number != 20);
  print(number > 20);
  print(number < 20);
  print(number >= 20);
  print(number <= 20);
  print('');

  print(number & 20);
  print(number | 20);
  print(number ^ 20);
  print(number << 2);
  print(number >> 2);
  print(number >>> 2);
  print('');

  print(true && true);
  print(false || true);
  print('');
}
