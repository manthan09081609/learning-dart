void main(List<String> args) {
  const int someInteger = 10;
  print(someInteger);
  const double someDouble = 10.2;
  print(someDouble);
  const String someString = 'Hello';
  print(someString);
  const bool someBool = true;
  print(someBool);
  const List<int> someList = [1, 2, 3, 4];
  print(someList);
  const Map<String, String> someMap = {'name': 'Manthan', 'gender': 'M'};
  print(someMap);
  const Set<int> someSet = {1, 2, 3};
  print(someSet);
  const dynamic someNull = null;
  print(someNull);
  const Symbol someSymbol = #someNull;
  print(someSymbol);
}
